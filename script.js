document.addEventListener('DOMContentLoaded', () => {
    // Language Switching Logic
    const langBtns = document.querySelectorAll('.lang-btn');
    let currentLang = localStorage.getItem('acvni_lang') || 'vi';

    function updateContent(lang) {
        // Update active button state
        langBtns.forEach(btn => {
            if (btn.dataset.lang === lang) {
                btn.classList.add('active');
            } else {
                btn.classList.remove('active');
            }
        });

        // Update text content
        document.querySelectorAll('[data-i18n]').forEach(element => {
            const key = element.getAttribute('data-i18n');
            const keys = key.split('.');
            let value = translations[lang];

            for (const k of keys) {
                if (value && value[k]) {
                    value = value[k];
                } else {
                    value = null;
                    break;
                }
            }

            if (value) {
                // Replace {year} with current year
                value = value.replace('{year}', new Date().getFullYear());

                if (element.tagName === 'INPUT' || element.tagName === 'TEXTAREA') {
                    element.placeholder = value;
                } else if (element.tagName === 'OPTION') {
                    element.text = value;
                } else {
                    element.innerHTML = value;
                }
            }
        });

        // Update HTML lang attribute
        document.documentElement.lang = lang;
        localStorage.setItem('acvni_lang', lang);
    }

    // Theme Toggle Logic removed - Forced Dark Mode


    // Initialize Language
    updateContent(currentLang);

    // Event Listeners for Language Buttons
    langBtns.forEach(btn => {
        btn.addEventListener('click', () => {
            currentLang = btn.dataset.lang;
            updateContent(currentLang);
        });
    });

    // Mobile Navigation Toggle
    const hamburger = document.querySelector('.hamburger');
    const navLinks = document.querySelector('.nav-links');
    const links = document.querySelectorAll('.nav-links li a'); // Only close on link click, not lang switch

    if (hamburger) {
        hamburger.addEventListener('click', () => {
            navLinks.classList.toggle('active');
            hamburger.classList.toggle('active');
        });
    }

    // Close mobile menu when clicking a link
    links.forEach(link => {
        link.addEventListener('click', () => {
            navLinks.classList.remove('active');
            if (hamburger) hamburger.classList.remove('active');
        });
    });

    // Smooth Scrolling for Anchor Links
    document.querySelectorAll('a[href^="#"]').forEach(anchor => {
        anchor.addEventListener('click', function (e) {
            e.preventDefault();

            const targetId = this.getAttribute('href');
            if (targetId === '#') return;

            const targetElement = document.querySelector(targetId);
            if (targetElement) {
                const headerOffset = 80;
                const elementPosition = targetElement.getBoundingClientRect().top;
                const offsetPosition = elementPosition + window.pageYOffset - headerOffset;

                window.scrollTo({
                    top: offsetPosition,
                    behavior: 'smooth'
                });
            }
        });
    });

    // Navbar Scroll Effect
    const header = document.getElementById('header');
    window.addEventListener('scroll', () => {
        if (window.scrollY > 10) {
            header.classList.add('scrolled');
        } else {
            header.classList.remove('scrolled');
        }
    });

    // Scroll Animation (Simple Intersection Observer)
    const observerOptions = {
        threshold: 0.1
    };

    const observer = new IntersectionObserver((entries) => {
        entries.forEach(entry => {
            if (entry.isIntersecting) {
                entry.target.style.opacity = '1';
                entry.target.style.transform = 'translateY(0)';
                observer.unobserve(entry.target);
            }
        });
    }, observerOptions);

    // Targets for animation: Service items, Zig-zag content, Cards, Gallery Images, Album elements
    const animatedElements = document.querySelectorAll('.service-item, .split-content, .featured-event-card, .section-intro, .showcase-image, .gallery-item, .album-hero-image, .grid-item, .album-info, .showcase-content');
    animatedElements.forEach((el, index) => {
        el.style.opacity = '0';
        el.style.transform = 'translateY(30px)';
        el.style.transition = `all 0.6s ease ${index * 0.1}s`;
        observer.observe(el);
    });
    // Hero Slideshow Logic
    const slides = document.querySelectorAll('.hero-slideshow .slide');
    let currentSlide = 0;

    if (slides.length > 0) {
        setInterval(() => {
            slides[currentSlide].classList.remove('active');
            currentSlide = (currentSlide + 1) % slides.length;
            slides[currentSlide].classList.add('active');
        }, 5000); // Change slide every 5 seconds
    }

    // Lightbox Logic (Restored)
    const lightboxImages = document.querySelectorAll('.grid-item img, .album-hero-image img');

    if (lightboxImages.length > 0) {
        // Create Modal HTML dynamically if not present
        if (!document.querySelector('.lightbox-modal')) {
            const modalHTML = `
                <div id="lightboxModal" class="lightbox-modal">
                    <span class="lightbox-close">&times;</span>
                    <img class="lightbox-content" id="lightboxImg">
                </div>
            `;
            document.body.insertAdjacentHTML('beforeend', modalHTML);
        }

        const modal = document.getElementById('lightboxModal');
        const modalImg = document.getElementById('lightboxImg');
        const closeBtn = document.querySelector('.lightbox-close');

        lightboxImages.forEach(img => {
            img.addEventListener('click', () => {
                modal.classList.add('active'); // Use class to toggle display flex
                modalImg.src = img.src;
                modalImg.alt = img.alt;
            });
        });

        // Close logic
        const closeLightbox = () => {
            modal.classList.remove('active');
        };

        if (closeBtn) closeBtn.addEventListener('click', closeLightbox);

        // Close on background click
        if (modal) {
            modal.addEventListener('click', (e) => {
                if (e.target === modal) {
                    closeLightbox();
                }
            });
        }
    }

    // Lightbox Logic Removed

    // Slider Logic (Featured Events & Gallery Albums)
    const sliderContainers = document.querySelectorAll('.events-slider-container');

    sliderContainers.forEach(container => {
        const track = container.querySelector('.slider-track');
        const slidesArr = Array.from(track.children);
        const nextButton = container.querySelector('.next-btn');
        const prevButton = container.querySelector('.prev-btn');

        if (track && slidesArr.length > 0) {
            let currentSlideIndex = 0;

            const updateSlidePosition = () => {
                const amountToMove = -100 * currentSlideIndex;
                track.style.transform = `translateX(${amountToMove}%)`;

                if (prevButton) prevButton.style.visibility = currentSlideIndex === 0 ? 'hidden' : 'visible';
                if (nextButton) nextButton.style.visibility = currentSlideIndex === slidesArr.length - 1 ? 'hidden' : 'visible';
            };

            updateSlidePosition();

            if (nextButton) {
                nextButton.addEventListener('click', () => {
                    if (currentSlideIndex < slidesArr.length - 1) {
                        currentSlideIndex++;
                        updateSlidePosition();
                    }
                });
            }

            if (prevButton) {
                prevButton.addEventListener('click', () => {
                    if (currentSlideIndex > 0) {
                        currentSlideIndex--;
                        updateSlidePosition();
                    }
                });
            }

            // Touch Swipe Support
            let touchStartX = 0;
            let touchEndX = 0;

            track.addEventListener('touchstart', (e) => {
                touchStartX = e.changedTouches[0].screenX;
            }, { passive: true });

            track.addEventListener('touchend', (e) => {
                touchEndX = e.changedTouches[0].screenX;
                handleSwipe();
            }, { passive: true });

            const handleSwipe = () => {
                const swipeThreshold = 50;
                if (touchEndX < touchStartX - swipeThreshold) {
                    if (currentSlideIndex < slidesArr.length - 1) {
                        currentSlideIndex++;
                        updateSlidePosition();
                    }
                } else if (touchEndX > touchStartX + swipeThreshold) {
                    if (currentSlideIndex > 0) {
                        currentSlideIndex--;
                        updateSlidePosition();
                    }
                }
            };

            if (slidesArr.length <= 1) {
                if (nextButton) nextButton.style.display = 'none';
                if (prevButton) prevButton.style.display = 'none';
            }
        }
    });

    // Generic Image Load Animation
    // Select images that are not part of the hero slideshow (as those have their own logic)
    const imagesToAnimate = document.querySelectorAll('img:not(.hero-slideshow img)');

    imagesToAnimate.forEach(img => {
        // Prepare image for animation
        img.style.opacity = '0';
        img.style.transform = 'translateY(20px)';
        img.style.transition = 'opacity 0.8s ease, transform 0.8s ease';

        const onImageLoad = () => {
            img.style.opacity = '1';
            img.style.transform = 'translateY(0)';

            // Remove inline styles after animation to allow CSS hover effects to work
            setTimeout(() => {
                img.style.transition = '';
                img.style.transform = '';
                img.style.opacity = '';
                // Add a class to ensure opacity stays 1 if needed by CSS (though default is usually 1)
                img.classList.add('loaded');
            }, 1000); // Wait slightly longer than transition (0.8s)
        };

        if (img.complete) {
            // If already loaded
            setTimeout(onImageLoad, 100);
        } else {
            // Wait for load
            img.addEventListener('load', onImageLoad);
        }
    });

    // Gallery Filtering Logic (Replace ScrollSpy)
    const filterLinks = document.querySelectorAll('.header-year-link');
    const yearGroups = document.querySelectorAll('.year-group');

    if (filterLinks.length > 0 && yearGroups.length > 0) {
        // Initial state: show active year
        const initialFilter = document.querySelector('.header-year-link.active').getAttribute('data-filter');
        filterYear(initialFilter);

        filterLinks.forEach(link => {
            link.addEventListener('click', (e) => {
                e.preventDefault();

                // Update active link
                filterLinks.forEach(l => l.classList.remove('active'));
                link.classList.add('active');

                // Filter content
                const filterValue = link.getAttribute('data-filter');
                filterYear(filterValue);
            });
        });

        function filterYear(yearId) {
            yearGroups.forEach(group => {
                if (group.id === yearId) {
                    group.style.display = 'block';
                    // Trigger animations for children if needed
                    const images = group.querySelectorAll('img');
                    images.forEach(img => {
                        img.style.opacity = '1';
                        img.style.transform = 'translateY(0)';
                    });
                } else {
                    group.style.display = 'none';
                }
            });
            // Scroll to top of main content slightly to ensure visibility
            window.scrollTo({ top: 0, behavior: 'smooth' });
        }
    }

    // Dynamic Image Loader for Consular Album (specific logic)
    if (window.location.pathname.includes('album_consular.html')) {
        const gridContainer = document.querySelector('.album-grid');
        // Check for images 2 through 10 (or more reasonably)
        // Since we cannot listDir in client JS easily without server listing, 
        // we can try to fetch them or assume a naming convention if user adds them.
        // User asked: "Can the album update if i add new images"
        // A simple way is to attempt to load image2.jpg, image3.jpg etc.

        const loadExtraImages = async () => {
            const baseUrl = 'assets/album2/image';
            const extensions = ['.jpg', '.webp', '.png', '.jpeg'];
            let imageIndex = 2; // Start from 2 since 1 is used
            let consecutiveFailures = 0;

            // Simple loop to try loading images
            while (consecutiveFailures < 3 && imageIndex < 20) {
                // Try each extension
                let found = false;
                for (let ext of extensions) {
                    const url = `${baseUrl}${imageIndex}${ext}`;
                    try {
                        const response = await fetch(url, { method: 'HEAD' });
                        if (response.ok) {
                            // Image exists, append to grid
                            // We'll create a new row or append to existing structure
                            // Simplified: Just append to a new flexible grid container or reuse grid-row-2 logic

                            // If it's the first extra image, maybe create a new container or just append to .album-grid
                            // Let's make a generic grid-row-2 for every 2 images found

                            // For simplicity in this static setup, let's just create a grid item and append it.
                            // We might need to restructure .album-grid to be a flex/grid wrap container instead of fixed rows

                            // Let's create a wrapper if not exists
                            let dynamicGrid = document.querySelector('.dynamic-photo-grid');
                            if (!dynamicGrid) {
                                dynamicGrid = document.createElement('div');
                                dynamicGrid.className = 'grid-row-2'; // Reuse 2-col layout
                                dynamicGrid.style.marginTop = '20px';
                                dynamicGrid.style.display = 'grid'; // Ensure grid
                                gridContainer.appendChild(dynamicGrid);
                            }

                            // If last row is full (2 items), make new one? 
                            // Actually grid-row-2 is 2 col. element. If we keep adding items to it, does it wrap?
                            // defined as grid-template-columns: 1fr 1fr;
                            // It will wrap if auto-flow is row.

                            const item = document.createElement('div');
                            item.className = 'grid-item';
                            item.innerHTML = `<img src="${url}" alt="More Event Photo ${imageIndex}">`;

                            // Add click listener for lightbox
                            item.querySelector('img').addEventListener('click', (e) => {
                                const modal = document.getElementById('lightboxModal');
                                const modalImg = document.getElementById('lightboxImg');
                                if (modal && modalImg) {
                                    modal.classList.add('active');
                                    modalImg.src = e.target.src;
                                }
                            });

                            dynamicGrid.appendChild(item);

                            found = true;
                            break; // Found this index
                        }
                    } catch (e) {
                        // Ignore error
                    }
                }

                if (found) {
                    consecutiveFailures = 0;
                } else {
                    consecutiveFailures++;
                }
                imageIndex++;
            }
        };

        loadExtraImages();
    }

});

// Combined Slider Logic (New merged Events/Gallery)
const combinedTrack = document.querySelector('.combined-slider-track');
const combinedSlides = Array.from(document.querySelectorAll('.combined-slide'));
const combinedNext = document.querySelector('.combined-btn-next');
const combinedPrev = document.querySelector('.combined-btn-prev');

if (combinedTrack && combinedSlides.length > 0) {
    let activeIndex = 0;

    function updateCombinedSlider() {
        combinedSlides.forEach((slide, index) => {
            if (index === activeIndex) {
                slide.classList.add('active');
            } else {
                slide.classList.remove('active');
            }
        });

        // Calculate centering
        const container = document.querySelector('.combined-slider-track-container');
        if (!container) return;
        const containerWidth = container.offsetWidth;
        const slideWidth = combinedSlides[0].offsetWidth;
        const slideStyle = window.getComputedStyle(combinedSlides[0]);
        const marginLeft = parseInt(slideStyle.marginLeft);
        const marginRight = parseInt(slideStyle.marginRight);
        const fullSlideWidth = slideWidth + marginLeft + marginRight;

        // Centering logic: offset moves the track so specified slide center hits container center
        const offset = (containerWidth / 2) - (slideWidth / 2) - (activeIndex * fullSlideWidth) - marginLeft;

        combinedTrack.style.transform = `translateX(${offset}px)`;
    }

    // Initial setup
    window.addEventListener('resize', updateCombinedSlider);
    // Small delay to ensure browser has calculated dimensions
    setTimeout(updateCombinedSlider, 200);

    if (combinedNext) {
        combinedNext.addEventListener('click', () => {
            activeIndex = (activeIndex + 1) % combinedSlides.length;
            updateCombinedSlider();
        });
    }

    if (combinedPrev) {
        combinedPrev.addEventListener('click', () => {
            activeIndex = (activeIndex - 1 + combinedSlides.length) % combinedSlides.length;
            updateCombinedSlider();
        });
    }

    // Touch Swipe
    let startX = 0;
    combinedTrack.addEventListener('touchstart', (e) => {
        startX = e.touches[0].clientX;
    }, { passive: true });

    combinedTrack.addEventListener('touchend', (e) => {
        const endX = e.changedTouches[0].clientX;
        const diff = startX - endX;
        if (Math.abs(diff) > 50) {
            if (diff > 0) combinedNext.click();
            else combinedPrev.click();
        }
    }, { passive: true });
}
